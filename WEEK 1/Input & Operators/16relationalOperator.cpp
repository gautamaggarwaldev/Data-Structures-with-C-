// RELATIONAL OPERATOR ----> (==, !=, >, <, <=, >=)
#include<iostream>
using namespace std;
int main(){
    // int x = 9;
    // cout << (x = 4);   OUTPUT ----> 4

    int x = 3, y = 5;
    cout << (x != y) << endl;
    cout << (x == y) << endl;
    cout << (x >= y) << endl;
    cout << (x <= y) << endl;
    cout << (x > y) << endl;
    cout << (x < y) << endl;
}